package com.librarymgt.model;

public class BooksWithCatName {
	
	public Integer id;
	
	public String bName;
	
	public String bCode;
	
	public String bAuthor;
	
	public Float bPrice;
	
	public Integer bRackno;
	
	public String bDateOfArrival;
	
	public String bookcategory;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getbName() {
		return bName;
	}

	public void setbName(String bName) {
		this.bName = bName;
	}

	public String getbCode() {
		return bCode;
	}

	public void setbCode(String bCode) {
		this.bCode = bCode;
	}

	public String getbAuthor() {
		return bAuthor;
	}

	public void setbAuthor(String bAuthor) {
		this.bAuthor = bAuthor;
	}

	public Float getbPrice() {
		return bPrice;
	}

	public void setbPrice(Float bPrice) {
		this.bPrice = bPrice;
	}

	public Integer getbRackno() {
		return bRackno;
	}

	public void setbRackno(Integer bRackno) {
		this.bRackno = bRackno;
	}

	public String getbDateOfArrival() {
		return bDateOfArrival;
	}

	public void setbDateOfArrival(String bDateOfArrival) {
		this.bDateOfArrival = bDateOfArrival;
	}

	public String getBookcategory() {
		return bookcategory;
	}

	public void setBookcategory(String bookcategory) {
		this.bookcategory = bookcategory;
	}
	
	
	
	
	
	

}
